function create_exclusion()

    local exclusion_table = {}
    local affiliations = {}
    local all_guns = {}

    for aff, guns in pairs(tog_table) do
        exclusion_table[aff] = {}
        table.insert_unique(affiliations, aff)
        for i, gun in ipairs(guns) do
            table.insert_unique(all_guns, gun)
        end
    end

    for _, aff in ipairs(affiliations) do
        for _, gun in ipairs(all_guns) do
            if not table.find(tog_table[aff], gun) then
                table.insert(exclusion_table[aff], gun)
            end
        end
    end

    local text = ''
    for aff, guns in pairs(exclusion_table) do
        text = text .. "\n" .. aff .. " = {"
        for i, gun in ipairs(guns) do
            text = text .. "'" .. gun .. "',"
        end
        text = text .. "},"
    end
    print(text)
end

